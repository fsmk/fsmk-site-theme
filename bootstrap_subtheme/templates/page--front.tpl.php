            <div class='col-sm-8 text-justify'>

          <p class="lead">

  Net neutrality is the fundamental principle of the Internet which
  guarantees that no discrimination is practiced on the resources or
  applications that the users try to access!
  </p>
  <p class="lead">
  Airtel is trying to undermine this basic principle by restricting the
  access by introducing differentiated price structure on accessing
  applications.
  </p>
  <p class="lead">
  Facebook is trying to do the same via Internet.org. Airtel's zero platform
  or Facebooks's internet.org may end up blocking specific online
  content, services or applications (protocols, websites, etc.), or the users'
  freedom to publish information!
  </p>
  <p class="lead">
  The Telecom Regulatory Authority(TRAI)is examining this issue to
  determine whether the 'openness' of the Internet is been compromised.

  Visit <a href="http://www.netneutrality.in/" target="_blank">netneutrality.in</a> for more detail.

  		</p>
  		<p class="lead">
  		As the time period for sending mail to TRAI ends on 24th April , we shall continue putting pressure on government
